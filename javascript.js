// Addition
function addition(arr) {
    return arr.reduce((accumulator, current) => accumulator + current , 0);
}

addition([1, 2, 3, 4, 5]);


// Subtraction
function subtraction(a, b) {
    return a - b;
}

subtraction(55, 24);


// Multiply
function muliplication(a, b) {
    return a * b;
}

muliplication(5, 8);

//Division
function division(a, b) {
    return a / b;
}

division(63, 9);